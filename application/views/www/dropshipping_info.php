            <h2>What is Dropshipping?</h2>
            <p>Dropshipping is not a new term, it has been around for quite some time. But there are also misconceptions on what drop shipping really is. We know dropshipping and we understand it. We have been dealing with thousands of legitimate drop ship suppliers for years.
            <br>Dropshipping is a service offered by (some, not all) Wholesalers. When you use a Wholesaler who offers Drop Shipping you don't have to worry about stocking or shipping products. Instead, when you get an order from a customer, you contact your wholesaler and they ship it to your customer for you. This means you, as a retailer, can sell a variety of products without ever having to stock any inventory whatsoever. This makes drop shipping very appealing for new, small, home-based online businesses. <br></p>
            <h2>How Drop Shipping Works:</h2>
            <p><br>No matter where you plan to sell products online (eBay, Amazon, Rakuten, Sears or even open your own online store) if you are starting a business selling products from home then you probably don't want to carry much, if any, inventory. Most people who are starting their first online business via auctions or online stores don't have a lot of money to buy a bunch of products or the space to warehouse and store the products they want to sell online. This is where working with a real Dropshipper will save you a ton of money.
            As stated earlier, a real Dropshipper is simply a Certified Wholesaler who offers dropshipping. Not all wholesalers offer dropshipping. In fact it's quite difficult to find genuine certified Wholesalers who will dropship and work with online sellers.
            For online sellers, working with a Dropshipper is pretty easy. You find the Dropshipper you want to work with (the one who has the products you want to sell.) They will then give you access to their product images so that you can post them on your website, auction, blog or however you plan to sell online. When a customer orders the product, you then order the product from the Wholesaler and they ship it to your customer. <br></p>
<h2>Pro's & Con's of Drop Shipping</h2>
<p>
Using a certified Wholesaler that dropships is a low-cost solution for people who work from home, don't have money to spend on inventory and want to sell products online. Drop shipping does work and it works very well but you do have to be careful with a few things.
First, you can't just sell any product you want and expect to be able to compete with drop shipping. When you have a product dropshipped, you are buying and selling 1 product at a time. That means your wholesale price is for 1 product when you have it dropshipped. Wholesalers always give discounts to retailers when they purchase in bulk. That means your wholesale price for 1 product (drop shipped) can be higher than a competitor who is buying in volume from the wholesaler...which means they can sell that product much cheaper than you can, and in some cases, even cheaper than your wholesale cost!
<br></p>
<h2>So how can drop shipping work? </h2>
<p>Easy. You have to pick the "right" products to sell; the right kinds of products to dropship. You can't assume that just because you might think a product is a 'good idea to sell' that you should jump in and start selling that product. Especially with dropshipping. Doing a little bit of market research before you start selling is critically important. You have to find the products you can have drop shipped that you can compete with in the current online market. But by doing a little bit of research first, you can be very successful with dropshipping!
    <br><br>
